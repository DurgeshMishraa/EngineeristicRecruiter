public class DateFormat {

    public String C_month(String month) {
        switch (month) {
            case "01":
                month = "Jan ";
                break;
            case "02":
                month = "Feb ";
                break;
            case "03":
                month = "Mar ";
                break;
            case "04":
                month = "Apr ";
                break;
            case "05":
                month = "May ";
                break;
            case "06":
                month = "Jun ";
                break;
            case "07":
                month = "Jul ";
                break;
            case "08":
                month = "Aug ";
                break;
            case "09":
                month = "Sep ";
                break;
            case "10":
                month = "Oct ";
                break;
            case "11":
                month = "Nov ";
                break;
            case "12":
                month = "Dec ";
                break;
            case "1":
                month = "Jan ";
                break;
            case "2":
                month = "Feb ";
                break;
            case "3":
                month = "Mar ";
                break;
            case "4":
                month = "Apr ";
                break;
            case "5":
                month = "May ";
                break;
            case "6":
                month = "Jun ";
                break;
            case "7":
                month = "Jul ";
                break;
            case "8":
                month = "Aug ";
                break;
            case "9":
                month = "Sep ";
                break;
            default:
                month = "";
                break;
        }

        return month;
    }


}
